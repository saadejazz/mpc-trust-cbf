def game_loop(args):
    """
    Main loop of the simulation. It handles updating all the HUD information,
    ticking the agent and, if needed, the world.
    """

    pygame.init()
    pygame.font.init()
    world = None

    try:
        if args.seed:
            random.seed(args.seed)

        client = carla.Client(args.host, args.port)
        client.set_timeout(10.0)

        traffic_manager = client.get_trafficmanager()
        sim_world = client.get_world()

        args.sync = False

        if args.sync:
            settings = sim_world.get_settings()
            settings.synchronous_mode = True
            settings.fixed_delta_seconds = 0.05
            traffic_manager.set_synchronous_mode(True)
            sim_world.apply_settings(settings)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)

        hud = HUD(args.width, args.height)
        world = World(client.load_world("Town05"), hud, args)
        ctrl = KeyboardControl(world)
        
        if args.sync:
            world.world.tick()
        

        start_x, start_y, start_yaw = get_current_pose(world.player.get_transform())
        start_point = np.array([start_x, start_y])
        end_point = np.array(END_POINT)
        
        world.controller = mpc_cbf.Controller(start_point, end_point)

        send_control_command(world.player, throttle=0.0, steer=0, brake=1.0)
        x_history     = [start_x]
        y_history     = [start_y]
        yaw_history   = [start_yaw]
        time_history  = [0]
        speed_history = [0]
        ped_loc_history = [[[loc.x, loc.y] for loc in [ped.get_location() for ped in world.pedestrians]]]


        ped_control = carla.WalkerControl()
        ped_control.speed = 0.25
        ped_control.direction.y = 0
        ped_control.direction.x = -1
        ped_control.direction.z = 0

        bone_transforms = []
        # bone_transforms.append(('crl_shoulder__L', carla.Transform(location = carla.Location(x=0.13), rotation=carla.Rotation(roll=-45))))
        # bone_transforms.append(('crl_foreArm__L', carla.Transform(location = carla.Location(z=-0.05, x=0.2, y=-0.03), rotation=carla.Rotation(pitch = -80))))
        # bone_transforms.append(('crl_head__C', carla.Transform(location = carla.Location(y = -0.12), rotation = carla.Rotation(roll = -40))))
        
        bone_transforms.append(('crl_shoulder__L', carla.Transform(location = carla.Location(x=0.13), rotation=carla.Rotation(roll=-90))))
        bone_transforms.append(('crl_foreArm__L', carla.Transform(location = carla.Location(z=-0.05, x=0.2), rotation=carla.Rotation(pitch = -160))))
        bone_transforms.append(('crl_head__C', carla.Transform(location = carla.Location(y = -0.12), rotation = carla.Rotation(roll = -70))))
        bone_control = carla.WalkerBoneControlIn(bone_transforms)
        
        world.pedestrians[0].set_bones(bone_control)
        world.pedestrians[0].blend_pose(0.45)
        frame = 0
        clock = pygame.time.Clock()
        print("Recording on file: %s" % client.start_recorder(args.recorder_filename, True))

        while (True):
            frame += 1
            clock.tick()
            if args.sync:
                world.world.tick()
            else:
                world.world.wait_for_tick()
            if ctrl.parse_events():
                return
            
            world.tick(clock)
            world.render(display)
            pygame.display.flip()

            # Update pose, timestamp
            current_x, current_y, current_yaw = \
                get_current_pose(world.player.get_transform())
            current_speed = world.player.get_velocity().length()

            # pedestrian 
            ped_locs = [[loc.x, loc.y] for loc in [ped.get_location() for ped in world.pedestrians]]
            ped_speeds = [[v.x, v.y] for v in [ped.get_velocity() for ped in world.pedestrians]]
            world.controller.perceive(ped_locs, ped_speeds)

            # Store history
            x_history.append(current_x)
            y_history.append(current_y)
            yaw_history.append(current_yaw)
            speed_history.append(current_speed)
            ped_loc_history.append(ped_locs)
            
            world.controller.update_waypoints(np.array([current_x, current_y]), end_point, current_speed)

            # Update the other controller values and controls
            current_timestamp = sim_world.get_snapshot().timestamp.elapsed_seconds
            # print(current_timestamp, time() - tic)
            world.controller.update_values(current_x, current_y, current_yaw, 
                                        current_speed,
                                        current_timestamp, world.player.get_velocity(), 
                                        frame, world.snaptime)
            world.controller.update_controls()
            cmd_throttle, cmd_steer, cmd_brake = world.controller.get_commands()
            time_history.append(current_timestamp)

            # Output controller command to CARLA server
            send_control_command(world.player,
                                    throttle=cmd_throttle,
                                    steer=cmd_steer,
                                    brake=cmd_brake)
            
            world.pedestrians[0].apply_control(ped_control)

            dist_to_end = np.linalg.norm(np.array([
                current_x - end_point[0],
                current_y - end_point[1]
            ]))

            if dist_to_end < DIST_THRESHOLD_TO_LAST_WAYPOINT:
                print("Finished")
                break

    finally:
        print("Stop recording")
        client.stop_recorder()

        with open("loc_history.pkl", "wb") as f:
            pickle.dump({
                "car_x": x_history,
                "car_y": y_history,
                "ped_data": ped_loc_history
            }, f)

        if world is not None:
            settings = world.world.get_settings()
            settings.synchronous_mode = False
            settings.fixed_delta_seconds = None
            world.world.apply_settings(settings)
            traffic_manager.set_synchronous_mode(True)
            world.destroy()

        pygame.quit()

