class World(object):
    """ Class representing the surrounding environment """
    
    controller = None

    def __init__(self, carla_world, hud, args):
        """Constructor method"""
        self._args = args
        self.world = carla_world
        try:
            self.map = self.world.get_map()
        except RuntimeError as error:
            print('RuntimeError: {}'.format(error))
            print('  The server could not send the OpenDRIVE (.xodr) file:')
            print('  Make sure it exists, has the same name of your town, and is correct.')
            sys.exit(1)
        self.hud = hud
        self.player = None
        self.pedestrians = []
        self.camera_manager = None
        self._weather_presets = find_weather_presets()
        self._weather_index = 0
        self._actor_filter = args.filter
        self._actor_generation = args.generation
        self.restart(args)
        self.world.on_tick(hud.on_world_tick)
        self.recording_enabled = True
        self.recording_start = 0
        self.snapshot = False
        self.snaptime = 0
        
        
    def save_camera_img(self, data):
        if self.controller is not None:
            self.snaptime = data.timestamp
            store_loc = str(self.snaptime).replace(".", "_")[: 3] 
            store_loc = f"rgb/image_{store_loc}.png"
            data.save_to_disk(store_loc)
            self.controller.update_trust(store_loc)
    
    def restart(self, args):
        """Restart the world"""
        # Keep same camera config if the camera manager exists.
        cam_index = self.camera_manager.index if self.camera_manager is not None else 0
        cam_pos_id = self.camera_manager.transform_index if self.camera_manager is not None else 0

        # Get a random blueprint.
        blueprint = get_actor_blueprints(self.world, self._actor_filter, self._actor_generation)
        blueprint = blueprint[3]
        blueprint.set_attribute('role_name', 'hero')
        if blueprint.has_attribute('color'):
            color = random.choice(blueprint.get_attribute('color').recommended_values)
            blueprint.set_attribute('color', color)

        # Spawn the player.
        if self.player is not None:
            spawn_point = self.player.get_transform()
            spawn_point.location.z += 2.0
            spawn_point.rotation.roll = 0.0
            spawn_point.rotation.pitch = 0.0
            self.destroy()
            self.player = self.world.try_spawn_actor(blueprint, spawn_point)
            self.modify_vehicle_physics(self.player)
        while self.player is None:
            if not self.map.get_spawn_points():
                print('There are no spawn points available in your map/town.')
                print('Please add some Vehicle Spawn Point to your UE4 scene.')
                sys.exit(1)
            spawn_points = self.map.get_spawn_points()
            spawn_point = spawn_points[8] if spawn_points else carla.Transform()
            
            # start point of the vehicle
            spawn_point.location.x = START_POINT[0]
            spawn_point.location.y = START_POINT[1]

            self.player = self.world.try_spawn_actor(blueprint, spawn_point)
            self.modify_vehicle_physics(self.player)
            print(self.player.id)

        # spawn pedestrians
        bp = get_actor_blueprints(self.world, 'walker', 'all')[8]
        transform = spawn_points[8]
        transform.location.y = 101
        transform.location.x = 37.5
        transform.rotation.yaw = 180.0
        transform.rotation.roll = 0.0
        transform.rotation.pitch = 0.0

        pedestrian = self.world.spawn_actor(bp, transform)
        self.pedestrians.append(pedestrian)

        # attach camera to player
        blueprint = self.world.get_blueprint_library().find('sensor.camera.rgb')
        blueprint.set_attribute('image_size_x', '1280')
        blueprint.set_attribute('image_size_y', '720')
        blueprint.set_attribute('fov', '45')
        blueprint.set_attribute('bloom_intensity', '0.0')
        blueprint.set_attribute('blur_amount', '0.0')
        blueprint.set_attribute('motion_blur_intensity', '0.0')
        blueprint.set_attribute('motion_blur_max_distortion', '0.0')
        blueprint.set_attribute('sensor_tick', '1.0')

        transform = carla.Transform(carla.Location(x = 1.5, z = 1.7))
        self.sensor = self.world.spawn_actor(blueprint, transform, attach_to = self.player)
        self.sensor.listen(lambda data: self.save_camera_img(data))

        if self._args.sync:
            self.world.tick()
        else:
            self.world.wait_for_tick()


        # Set up the sensors.
        self.camera_manager = CameraManager(self.player, self.hud)
        self.camera_manager.transform_index = cam_pos_id
        self.camera_manager.set_sensor(cam_index, notify=False)
        actor_type = get_actor_display_name(self.player)
        self.hud.notification(actor_type)

    def next_weather(self, reverse=False):
        """Get next weather setting"""
        self._weather_index += -1 if reverse else 1
        self._weather_index %= len(self._weather_presets)
        preset = self._weather_presets[self._weather_index]
        self.hud.notification('Weather: %s' % preset[1])
        self.player.get_world().set_weather(preset[0])

    def modify_vehicle_physics(self, actor):
        #If actor is not a vehicle, we cannot use the physics control
        try:
            physics_control = actor.get_physics_control()
            physics_control.use_sweep_wheel_collision = True
            actor.apply_physics_control(physics_control)
        except Exception:
            pass

    def tick(self, clock):
        """Method for every tick"""
        self.hud.tick(self, clock)

    def render(self, display):
        """Render world"""
        self.camera_manager.render(display)
        self.hud.render(display)

    def destroy_sensors(self):
        """Destroy sensors"""
        self.camera_manager.sensor.destroy()
        self.camera_manager.sensor = None
        self.camera_manager.index = None

    def destroy(self):
        """Destroys all actors"""
        actors = [
            self.camera_manager.sensor,
            self.player] + self.pedestrians
        for actor in actors:
            if actor is not None:
                actor.destroy()


